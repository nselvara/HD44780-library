//******************************************************************
// Defines
//******************************************************************
/*
#define LCD_Clear           0x01        // Replaces all characters with ASCII 'space'.
#define LCD_Home            0x02        // Returns the cursor to first position on first line.
#define LCD_EntryMode       0x06        // Shifts cursor from left to right on read/write.
#define LCD_DisplayOff      0x08        // Turns display off.
#define LCD_DisplayOn       0x0C        // Display on, cursor off, don't blink character.
#define LCD_FunctionSet     0x38        // Sets the data-width at 8-bit, 2-line resp. 4-line and 5x10 dots.
#define LCD_FunctionReset   0x30        // Resets the LCD.
#define LCD_CursorShowBlink 0x03        // Show cursor and blink for display/off control.
#define LCD_SetCursor       0x80        // Sets cursor position.
*/

//******************************************************************
// LCD Commands
//******************************************************************
typedef enum LCD_Commands {
  LCD_Clear = 0x01,
  LCD_Home = 0x02,
  LCD_EntryMode = 0x06,
  LCD_DisplayOff = 0x08,
  LCD_DisplayOn = 0x0C,
  LCD_FunctionSet = 0x38,
  LCD_FunctionReset = 0x30,
  LCD_CursorShowBlink = 0x03,
  LCD_SetCursor = 0x80
} lcd_command_t;

//******************************************************************
// Function prototypes
//******************************************************************
// 1: Sets display direction from left-to-right and cursor placement in incremental way.
// 2: Clears the LCD.
// 3: Puts the command on the data path of LCD.
// 4: Writes out a string one by one as an ASCII code.
// 5: Same like 9, additionally it supports cursor placement.
// 6: Prints one char in ASCII format.
// 7: Same like 9, additionally it supports format-string (e.g. %i) like printf.
// 8: Sends command to LCD where the next char will be printed.
// 9: Converts special character if there is one and returns the necessary code.
// 10: Reverts an 8-bit int number.
// 11: Reverts a string resp. array.
// 12: Converts integer to string.
// 13: Prints three zeroes if the coordinate value is zero.
// 14: Converts a decimal number to hex in ASCII code.
//*******************************************************************

void lcd_init               (void);
void lcd_clear              (void);
void lcd_command            (lcd_command_t command);
void lcd_write              (charptr_t string);
void lcd_write_xy           (charptr_t string, int row, int column);
void lcd_write_char         (int ascii);
void lcd_print              (charptr_t string,...); //__attribute__ ((format (printf, 1, 2)));      // Tell the compiler to treat the function like printf (), so the compiler can look for type safety likewise with printf ().
void cursor                 (int row, int column);
char special_character      (char character);
int  reverse_char           (int x);
void reverse                (char s[]);
void itoa                   (int n, char s[]);
void significant_zero_place (float number);
charptr_t hex_to_ascii      (uint32_t hex);

#endif /* LCD_H_ */
