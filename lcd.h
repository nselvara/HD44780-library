/******************************* Program Notes ******************************
 *  This program uses an 8-bit interface to communicate with the display.
 *  The busy-flag isn't checked, hence, the timing delays are longer
 *  than the specified minimum of the respective data-sheet.
 ****************************************************************************
 *
 ********************************** Pinout **********************************
 *
 *           ----------------                   ----------------
 *          |   MicroBlaze   |                 |       LCD      |
 *          |                |                 |                |
 *          |  GPIO_LCD (11) |---------------->| D7             |
 *          |  GPIO_LCD (10) |---------------->| D6             |
 *          |  GPIO_LCD (09) |---------------->| D5             |
 *          |  GPIO_LCD (08) |---------------->| D4             |
 *          |  GPIO_LCD (08) |---------------->| D3             |
 *          |  GPIO_LCD (07) |---------------->| D2             |
 *          |  GPIO_LCD (06) |---------------->| D1             |
 *          |  GPIO_LCD (05) |---------------->| D0             |
 *          |                |                 |                |
 *          |  GPIO_LCD (04) |---------------->| RS             |
 *          |  GPIO_LCD (03) |---------------->| RW             |
 *          |  GPIO_LCD (02) |---------------->| EN             |
 *           ----------------                   ----------------
 *
 *********************************** End ***********************************/

